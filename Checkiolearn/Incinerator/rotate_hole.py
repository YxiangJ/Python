# 熟悉循环数据结构


from collections import deque


def rotate(state, pipe_numbers):
    state = deque(state)
    good_values = []
    for i in range(len(state)):
        if all(state[j] for j in pipe_numbers):
            good_values.append(i)
        state.rotate(1)
    return good_values


if __name__ == '__main__':
    # These "asserts" using only for self-checking and not necessary for auto-testing
    assert rotate([1, 0, 0, 0, 1, 1, 0, 1, 0, 0, 0, 1],
                  [0, 1]) == [1, 8], "Example"
    assert rotate([1, 0, 0, 0, 1, 1, 0, 1, 0, 0, 0, 1],
                  [0, 1, 2]) == [], "Mission impossible"
    assert rotate([1, 0, 0, 0, 1, 1, 0, 1], [0, 4, 5]) == [0], "Don't touch it"
    assert rotate([1, 0, 0, 0, 1, 1, 0, 1], [5, 4, 5]) == [
        0, 5], "Two cannonballs in the same pipe"
    print("well done!")
